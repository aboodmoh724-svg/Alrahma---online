export function getSurahByName(name: string): SurahMetadata | undefined {
  return QURAN_SURAHS.find(
    (s) => s.name.trim() === name.replace("سورة", "").trim()
  );
}

export function getSurahsForPage(page: number): SurahMetadata[] {
  return QURAN_SURAHS.filter((s) => page >= s.startPage && page <= s.endPage);
}
